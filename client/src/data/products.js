// ─── Helper functions ─────────────────────────────────────────

export function getProductBySlug(slug) {
  return products.find(p => p.slug === slug)
}

export function getProductsByCategory(category) {
  return products.filter(p => p.category === category)
}

export function getProductsBySubcategory(subcategory) {
  return products.filter(p => p.subcategory === subcategory)
}

export function getProductsByParentSubcategory(parentSub) {
  return products.filter(p => p.parentSubcategory === parentSub)
}

export function getAllProducts() {
  return products
}

export function getRelatedProducts(slug, limit = 4) {
  const product = getProductBySlug(slug)
  if (!product) return []
  return products
    .filter(p => p.slug !== slug && (p.subcategory === product.subcategory || p.category === product.category))
    .slice(0, limit)
}

